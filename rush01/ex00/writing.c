#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	write(1, str, i);
}

void	ft_display(char *tab)
{
	int	i;
	int	j;

	j = 0;
	while (j < 4)
	{
		i = 0;
		while (i < 4)
		{
			ft_putchar(tab[i * 4 + j]);
			if (i < 3)
				ft_putchar(' ');
			else
				ft_putchar('\n');
			i++;
		}
		j++;
	}
}
